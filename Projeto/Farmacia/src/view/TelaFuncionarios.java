    private void carregarFuncionarios() {
        listaModelFuncionarios = controllerFuncionarios.getListaFuncionariosController();
        DefaultTableModel model = (DefaultTableModel) tbFuncionarios.getModel();
        //número de linhas que a tabela começa
        model.setNumRows(0);

        //a variável cont recebe o tamanho da lista
        int cont = listaModelFuncionarios.size();
        //inserir clientes na tabela
        for (int c = 0; c < cont; c++) {
            model.addRow(new Object[]{
                listaModelFuncionarios.get(c).getIdFun(),
                listaModelFuncionarios.get(c).getNomeFun(),
                listaModelFuncionarios.get(c).getCargoFun(),
                listaModelFuncionarios.get(c).getCidadeFun()

            });
        }
    }

