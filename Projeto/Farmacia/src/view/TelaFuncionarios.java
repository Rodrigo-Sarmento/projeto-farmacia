    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        modelFuncionarios = new ModelFuncionarios();
        modelFuncionarios.setBairroFun(txtBairro.getText());
        modelFuncionarios.setCepFun(txtCEP.getText());
        modelFuncionarios.setCidadeFun(txtCidade.getText());
        modelFuncionarios.setEnderecoFun(txtEndereco.getText());
        modelFuncionarios.setNomeFun(txtNome.getText());
        modelFuncionarios.setTelefoneFun(txtTelefone.getText());
        modelFuncionarios.setEstadoFun(boxEstado.getSelectedItem().toString());
        modelFuncionarios.setCargoFun(txtCargo.getText());
        modelFuncionarios.setSalarioFun(Double.parseDouble(txtSalario.getText()));
        modelFuncionarios.setCpfFun(txtCPF.getText());

        if (salvarAlterar.equals("salvar")) {
            if (controllerFuncionarios.salvarFuncionariosController(modelFuncionarios) > 0) {
                JOptionPane.showMessageDialog(this, "Funcionario cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                this.limparCampos();
                this.carregarFuncionarios();
                salvarAlterar = "salvar";
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastar funcionário!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            modelFuncionarios.setIdFun(Integer.parseInt(txtCodigo.getText()));
            if (controllerFuncionarios.atualizarFuncionariosController(modelFuncionarios)) {
                JOptionPane.showMessageDialog(this, "Funcionário alterado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                this.limparCampos();
                this.carregarFuncionarios();
                salvarAlterar = "salvar";
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar funcionário!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

