    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int quantidade = tbProdutos.getRowCount();
        if (quantidade < 1) {
            JOptionPane.showMessageDialog(this, "NÃ£o existe itens para excluir!", "Alerta", JOptionPane.WARNING_MESSAGE);
        } else {
            DefaultTableModel modelo = (DefaultTableModel) tbProdutos.getModel();
            int linha = Integer.parseInt(JOptionPane.showInputDialog("Informe o item que deseja excluir"));
            modelo.removeRow(linha - 1);
            txtBruto.setText(somaValorTotal() + "");

            for (int i = 0; i < quantidade; i++) {
                modelo.setValueAt(i + 1, i, 0);
            }
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

