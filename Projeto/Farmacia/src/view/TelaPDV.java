    private void limparTela() {
        txtBruto.setText("");
        DefaultTableModel modelo = (DefaultTableModel) tbProdutos.getModel();
        modelo.setNumRows(0);
        lblStatus.setText("Caixa Livre");
    }

    private void pegarConteudo(java.awt.event.KeyEvent e) {
        lblStatus.setText("Venda em aberto");
        DefaultTableModel modelo = (DefaultTableModel) tbProdutos.getModel();
        if (e.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            try {
                modelProduto = controllerProduto.retornarProdutoController(Integer.parseInt(txtCodigoProduto.getText()));
                modelo.addRow(new Object[]{
                    modelo.getRowCount() + 1,
                    modelProduto.getId(),
                    modelProduto.getNome(),
                    quantidade,
                    modelProduto.getValorVenda(),
                    modelProduto.getValorVenda() * quantidade
                });
                txtBruto.setText(somaValorTotal() + "");
                txtCodigoProduto.setText("");
                quantidade = 1;
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Incluir apenas nÃºmeros nesse campo !", "Alerta", JOptionPane.WARNING_MESSAGE);
            }

        }

    }

    private float somaValorTotal() {
        float soma = 0;
        float valor = 0;
        int cont = tbProdutos.getRowCount();

        for (int i = 0; i < cont; i++) {
            valor = Float.parseFloat(String.valueOf(tbProdutos.getValueAt(i, 5)));
            soma += valor;
        }
        return soma;
    }

    private void setarOperador() {
        lblOperador.setText(modelSessaoUsuario.nome);
    }

    private void salvarVenda() {
        int cont;
        int codigoProduto = 0;
        int codigoVenda = 0;
        modelVendas = new ModelVendas();
        modelVendas.setCliente(4);
        try {
            modelVendas.setVenDataVenda(formatadorData.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {
            Logger.getLogger(TelaPDV.class.getName()).log(Level.SEVERE, null, ex);
        }

        modelVendas.setVenDesconto(telaPagamentoPDV.getDesconto());
        modelVendas.setVenValorBruto(Double.parseDouble(txtBruto.getText()));
        modelVendas.setVenValorLiquido(formatadorMascara.arredondamentoComPontoDuasCasas(telaPagamentoPDV.getValorTotal()));
        //salvar venda
        codigoVenda = controllerVendas.salvarVendasController(modelVendas);

        cont = tbProdutos.getRowCount();

        for (int i = 0; i < cont; i++) {
            codigoProduto = (int) tbProdutos.getValueAt(i, 1);
            modelVendasProdutos = new ModelVendasProdutos();
            modelProduto = new ModelProduto();
            modelVendasProdutos.setProduto(codigoProduto);
            modelVendasProdutos.setVendas(codigoVenda);
            modelVendasProdutos.setVenProValor((double) tbProdutos.getValueAt(i, 4));
            modelVendasProdutos.setVenProQuantidade(Integer.parseInt(tbProdutos.getValueAt(i, 3).toString()));
            //produto
            modelProduto.setId(codigoProduto);
            modelProduto.setQuantidade(controllerProduto.retornarProdutoController(codigoProduto).getQuantidade()
                    - Integer.parseInt(tbProdutos.getValueAt(i, 3).toString()));
            listaModelVendasProdutos.add(modelVendasProdutos);
            listaModelProdutos.add(modelProduto);
        }
        //salvar os produtos da venda
        if (controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutos)) {
            //alterando a quantidade de produtos
            controllerProduto.atualizarProdutoController(listaModelProdutos);
            this.limparTela();
        } else {
            JOptionPane.showMessageDialog(this, "erro", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

