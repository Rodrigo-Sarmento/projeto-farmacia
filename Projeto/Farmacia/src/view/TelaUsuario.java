    private void carregarUsuario(){
        listaModelUsuario = controllerUsuario.getListaUsuarioController();
        DefaultTableModel model = (DefaultTableModel) tbUsuarios.getModel();
        //número de linhas que a tabela começa
        model.setNumRows(0);
        
        //a variável cont recebe o tamanho da lista
        int cont = listaModelUsuario.size();
        //inserir usuários na tabela
        for(int c =0; c<cont;c++){
            model.addRow(new Object[]{
                listaModelUsuario.get(c).getIdUsuario(),
                listaModelUsuario.get(c).getUsuNome(),
                listaModelUsuario.get(c).getUsuLogin()
           
            });
        }
    }
    
