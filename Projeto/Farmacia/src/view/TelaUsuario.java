    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       int linha = tbUsuarios.getSelectedRow();
       int codigo = 0;
        if(linha<0){
            JOptionPane.showMessageDialog(this,"Selecione um usuário na tabela, antes de clicar em excluir!","Atenção",JOptionPane.WARNING_MESSAGE);
        }else{
            String nome = (String)tbUsuarios.getValueAt(linha,1);
            int opcao= JOptionPane.showConfirmDialog(this,"Tem certeza que deseja excluir o usuário: "+nome,"Atenção",JOptionPane.YES_NO_OPTION);
            if(opcao==JOptionPane.OK_OPTION){
                codigo = (Integer) tbUsuarios.getValueAt(linha,0);
                controllerUsuario.excluirUsuarioController(codigo);
        }
       }
        carregarUsuario();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
       salvarAlterar="alterar";
       modelUsuario = new ModelUsuario();
       int linha = tbUsuarios.getSelectedRow();
       int codigo = 0;
        if(linha<0){
            JOptionPane.showMessageDialog(this,"Selecione um usuário na tabela, antes de clicar em alterar!","Atenção",JOptionPane.WARNING_MESSAGE);
        }else{
            codigo = (int) tbUsuarios.getValueAt(linha,0);
            modelUsuario = controllerUsuario.getUsuarioController(codigo);
            txtCodigo.setText(modelUsuario.getIdUsuario()+"");
            txtLogin.setText(modelUsuario.getUsuLogin());
            txtNome.setText(modelUsuario.getUsuNome());
            txtSenha.setText(modelUsuario.getUsuSenha());
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       modelUsuario = new ModelUsuario();
       modelUsuario.setUsuNome(txtNome.getText());
       modelUsuario.setUsuLogin(txtLogin.getText());
       modelUsuario.setUsuSenha(txtSenha.getText());

       if(salvarAlterar.equals("salvar")){
        if(controllerUsuario.salvarUsuarioController(modelUsuario)>0){
           JOptionPane.showMessageDialog(this,"Usuário cadastrado com sucesso!","Sucesso",JOptionPane.INFORMATION_MESSAGE);
           this.limparCampos();
           this.carregarUsuario();
           salvarAlterar="salvar";
        }else{
           JOptionPane.showMessageDialog(this,"Erro ao cadastar usuário!","ERRO",JOptionPane.ERROR_MESSAGE);
        }
      }else{
           modelUsuario.setIdUsuario(Integer.parseInt(txtCodigo.getText()));
           if(controllerUsuario.atualizarUsuarioController(modelUsuario)){
               JOptionPane.showMessageDialog(this,"Usuário alterado com sucesso!","Sucesso",JOptionPane.INFORMATION_MESSAGE);
               this.limparCampos();
               this.carregarUsuario();
               salvarAlterar="salvar";
           }else{
               JOptionPane.showMessageDialog(this,"Erro ao alterar usuário!","ERRO",JOptionPane.ERROR_MESSAGE);
           }
       }
    }//GEN-LAST:event_btnSalvarActionPerformed

